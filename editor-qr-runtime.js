'use strict';

/**
 * Reliable QR generation and persistence for the card editor.
 *
 * QR libraries are served from the application origin so generation does not
 * depend on third-party CDNs. Generated automatic QR images are also included
 * in the saved editor state and restored when a design is reopened.
 */
(function installEditorQrRuntime(global) {
  const STYLING_LIBRARY_URL = '/nfc/vendor/qr-code-styling.js?v=1.5.0';
  const BASIC_LIBRARY_URL = '/nfc/vendor/qrcode.min.js?v=1.0.0';
  const AUTO_SOURCES = new Set(['auto-card', 'auto-vcard']);
  let activeCardGeneration = null;

  if (
    typeof Config === 'undefined' ||
    typeof CardManager === 'undefined' ||
    typeof StateManager === 'undefined'
  ) {
    console.error('[EditorQrRuntime] Editor dependencies are unavailable.');
    return;
  }

  Config.SCRIPT_URLS.qrCodeStyling = STYLING_LIBRARY_URL;
  Config.SCRIPT_URLS.qrcode = BASIC_LIBRARY_URL;

  function isEnglish() {
    return document.documentElement.lang === 'en';
  }

  function message(arabic, english) {
    return isEnglish() ? english : arabic;
  }

  function currentQrSource() {
    return document.querySelector('input[name="qr-source"]:checked')?.value || 'auto-card';
  }

  function blobToDataUrl(blob) {
    return new Promise((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => resolve(reader.result);
      reader.onerror = () => reject(reader.error || new Error('Could not read generated QR image.'));
      reader.readAsDataURL(blob);
    });
  }

  function getLogoUrl() {
    const rawUrl = DOMElements.draggable?.logoImg?.src || '';
    if (!rawUrl || rawUrl.includes('mc-prime-nfc.png')) return null;

    const useLogo = document.getElementById('qr-use-logo');
    if (!useLogo?.checked) return null;

    return typeof sanitizeURL === 'function' ? sanitizeURL(rawUrl) : rawUrl;
  }

  function buildQrOptions(data, image) {
    const dotsColor = document.getElementById('qr-dots-color') || document.getElementById('qr-color');
    const backgroundColor = document.getElementById('qr-bg-color');
    const dotsType = document.getElementById('qr-dots-type');
    const cornersType = document.getElementById('qr-corners-type');
    const color = dotsColor?.value || '#000000';

    return {
      width: 300,
      height: 300,
      data,
      image,
      dotsOptions: { color, type: dotsType?.value || 'rounded' },
      backgroundOptions: { color: backgroundColor?.value || '#ffffff' },
      imageOptions: { crossOrigin: 'anonymous', margin: 10, imageSize: 0.4 },
      cornersSquareOptions: { type: cornersType?.value || 'extra-rounded', color }
    };
  }

  async function renderQrDataUrl(options) {
    const container = DOMElements.qrCodeTempGenerator;
    if (container) container.replaceChildren();

    try {
      const qr = new QRCodeStyling(options);
      if (container) qr.append(container);
      const blob = await qr.getRawData('png');
      if (!blob) throw new Error('QR renderer returned an empty image.');
      return await blobToDataUrl(blob);
    } finally {
      if (container) container.replaceChildren();
    }
  }

  async function renderWithLogoFallback(options) {
    try {
      return await renderQrDataUrl(options);
    } catch (error) {
      if (!options.image) throw error;
      console.warn('[EditorQrRuntime] Logo could not be embedded; retrying without it.', error);
      return renderQrDataUrl({ ...options, image: null });
    }
  }

  function installStatePersistence() {
    if (StateManager.__qrPersistenceInstalled) return;

    const originalGetStateObject = StateManager.getStateObject;
    const originalApplyState = StateManager.applyState;

    StateManager.getStateObject = function getStateObjectWithQr() {
      const state = originalGetStateObject.apply(this, arguments);
      const source = state.inputs?.['qr-source'] || currentQrSource();
      if (AUTO_SOURCES.has(source) && CardManager.autoGeneratedQrDataUrl) {
        state.imageUrls ||= {};
        state.imageUrls.qrCode = CardManager.autoGeneratedQrDataUrl;
      }
      return state;
    };

    StateManager.applyState = function applyStateWithQr(state) {
      const result = originalApplyState.apply(this, arguments);
      const source = state?.inputs?.['qr-source'];
      const savedQr = state?.imageUrls?.qrCode;
      if (AUTO_SOURCES.has(source) && savedQr) {
        CardManager.autoGeneratedQrDataUrl = savedQr;
        CardManager.qrCodeImageUrl = savedQr;
        CardManager.updateQrCodeDisplay();
      }
      return result;
    };

    Object.defineProperty(StateManager, '__qrPersistenceInstalled', {
      value: true,
      configurable: false,
      enumerable: false
    });
  }

  async function generateCardLinkQr() {
    if (activeCardGeneration) return activeCardGeneration;

    activeCardGeneration = (async () => {
      const button = DOMElements.buttons?.generateAutoQr;
      UIManager.setButtonLoadingState(
        button,
        true,
        message('جاري إنشاء وحفظ QR...', 'Generating and saving QR...')
      );

      try {
        await Utils.loadScript(STYLING_LIBRARY_URL);
        if (typeof QRCodeStyling !== 'function') {
          throw new Error('The local QR renderer did not initialize.');
        }

        let designId = Config.currentDesignId;
        if (!designId) designId = await ShareManager.saveDesign();
        if (!designId) {
          throw new Error(message('فشل حفظ التصميم اللازم لإنشاء الرابط.', 'Failed to save the design required for the link.'));
        }

        const viewerUrl = new URL(isEnglish() ? 'viewer-en.html' : 'viewer.html', window.location.href);
        viewerUrl.searchParams.set('id', designId);

        const options = buildQrOptions(viewerUrl.href, getLogoUrl());
        const dataUrl = await renderWithLogoFallback(options);
        CardManager.autoGeneratedQrDataUrl = dataUrl;
        CardManager.qrCodeImageUrl = dataUrl;
        CardManager.updateQrCodeDisplay();

        const persistedId = await ShareManager.saveDesign(StateManager.getStateObject());
        if (!persistedId) {
          throw new Error(message('تعذر حفظ صورة QR داخل الكارت.', 'The QR image could not be saved with the card.'));
        }

        StateManager.save();
        UIManager.announce(message('تم إنشاء QR وحفظه داخل الكارت بنجاح.', 'QR Code was generated and saved with the card.'));
        return persistedId;
      } catch (error) {
        console.error('[EditorQrRuntime] Failed to generate card QR:', error);
        const title = message(
          'حدث خطأ. لم نتمكن من إنشاء وحفظ رابط QR Code.',
          'An error occurred. We could not generate and save the QR Code link.'
        );
        alert(`${title}\n\n${error?.message || error}`);
        return null;
      } finally {
        UIManager.setButtonLoadingState(button, false);
      }
    })();

    try {
      return await activeCardGeneration;
    } finally {
      activeCardGeneration = null;
    }
  }

  installStatePersistence();
  CardManager.generateCardLinkQr = generateCardLinkQr;

  global.EditorQrRuntime = Object.freeze({
    STYLING_LIBRARY_URL,
    BASIC_LIBRARY_URL,
    generateCardLinkQr,
    renderQrDataUrl,
    whenIdle: () => activeCardGeneration || Promise.resolve()
  });
})(window);
